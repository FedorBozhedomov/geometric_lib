def area(a, h):
    '''
    Получает на вход a - длину основания прямоугольного треугольника и h - длину его высоты.
    Возвращает значение его площади.
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''Принимает длины трех сторон треугольника (a, b, c), возвращает его периметр.'''
    return a + b + c 


